#!/usr/bin/env node

import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import {
  getAdapterById,
} from '../dist/hooks/autopilot/adapters/index.js';
import { DEFAULT_PIPELINE_CONFIG } from '../dist/hooks/autopilot/pipeline-types.js';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const taskToken = '__OMC_NAMED_WORKFLOW_TASK__';
const taskDisplayToken = '__OMC_NAMED_WORKFLOW_TASK_DISPLAY__';
const analystPromptToken = '__OMC_NAMED_WORKFLOW_ANALYST_PROMPT__';
const architectPromptToken = '__OMC_NAMED_WORKFLOW_ARCHITECT_PROMPT__';

function serializeTaskContexts(prompt) {
  const serialized = prompt
    .replaceAll(`**Original Idea:** "${taskToken}"`, `**Original Idea:**\n\n    ${taskDisplayToken}`)
    .replace(
      `prompt="REQUIREMENTS ANALYSIS for: ${taskToken}\n\nExtract and document:\n1. Functional requirements (what it must do)\n2. Non-functional requirements (performance, UX, etc.)\n3. Implicit requirements (things user didn't say but needs)\n4. Out of scope items\n\nOutput as structured markdown with clear sections."`,
      `prompt=${analystPromptToken}`,
    )
    .replace(
      `prompt="TECHNICAL SPECIFICATION for: ${taskToken}\n\nBased on the requirements analysis above, create:\n1. Tech stack decisions with rationale\n2. Architecture overview (patterns, layers)\n3. File structure (directory tree)\n4. Dependencies list (packages)\n5. API/interface definitions\n\nOutput as structured markdown."`,
      `prompt=${architectPromptToken}`,
    );
  if (serialized.includes(taskToken)) {
    throw new Error("Unclassified named workflow task placeholder in stage prompt");
  }
  return serialized;
}


function analystPrompt(task) {
  return `REQUIREMENTS ANALYSIS for: ${task}\n\nExtract and document:\n1. Functional requirements (what it must do)\n2. Non-functional requirements (performance, UX, etc.)\n3. Implicit requirements (things user didn't say but needs)\n4. Out of scope items\n\nOutput as structured markdown with clear sections.`;
}

function architectPrompt(task) {
  return `TECHNICAL SPECIFICATION for: ${task}\n\nBased on the requirements analysis above, create:\n1. Tech stack decisions with rationale\n2. Architecture overview (patterns, layers)\n3. File structure (directory tree)\n4. Dependencies list (packages)\n5. API/interface definitions\n\nOutput as structured markdown.`;
}
const stages = ['ralplan', 'execution', 'ralph', 'qa'];
const prompts = Object.fromEntries(stages.map((stage) => [
  stage,
  serializeTaskContexts(getAdapterById(stage).getPrompt({
    idea: taskToken,
    directory: '.',
    config: DEFAULT_PIPELINE_CONFIG,
  })),
]));

const generated = `// Generated by scripts/build-workflow-stage-prompts.mjs from the canonical TypeScript stage adapters.\n// Do not edit this file directly.\n\nconst TASK_DISPLAY_TOKEN = ${JSON.stringify(taskDisplayToken)};\nconst ANALYST_PROMPT_TOKEN = ${JSON.stringify(analystPromptToken)};\nconst ARCHITECT_PROMPT_TOKEN = ${JSON.stringify(architectPromptToken)};\nconst PROMPTS = Object.freeze(${JSON.stringify(prompts, null, 2)});\n\nfunction analystPrompt(task) {\n  return \`REQUIREMENTS ANALYSIS for: \${task}\\n\\nExtract and document:\\n1. Functional requirements (what it must do)\\n2. Non-functional requirements (performance, UX, etc.)\\n3. Implicit requirements (things user didn't say but needs)\\n4. Out of scope items\\n\\nOutput as structured markdown with clear sections.\`;\n}\n\nfunction architectPrompt(task) {\n  return \`TECHNICAL SPECIFICATION for: \${task}\\n\\nBased on the requirements analysis above, create:\\n1. Tech stack decisions with rationale\\n2. Architecture overview (patterns, layers)\\n3. File structure (directory tree)\\n4. Dependencies list (packages)\\n5. API/interface definitions\\n\\nOutput as structured markdown.\`;\n}\n\nexport function resolveCanonicalWorkflowStagePrompt(stageId, task) {\n  const prompt = PROMPTS[stageId];\n  if (typeof prompt !== 'string' || typeof task !== 'string' || task.trim().length === 0) return null;\n  const normalizedTask = task.trim();\n  const replacements = {\n    [TASK_DISPLAY_TOKEN]: JSON.stringify(normalizedTask),\n    [ANALYST_PROMPT_TOKEN]: JSON.stringify(analystPrompt(normalizedTask)),\n    [ARCHITECT_PROMPT_TOKEN]: JSON.stringify(architectPrompt(normalizedTask)),\n  };\n  return prompt.replace(/__OMC_NAMED_WORKFLOW_(?:TASK_DISPLAY|ANALYST_PROMPT|ARCHITECT_PROMPT)__/g, (token) => replacements[token]);\n}\n`;

for (const relativePath of [
  'scripts/lib/workflow-stage-prompts.mjs',
  'templates/hooks/lib/workflow-stage-prompts.mjs',
]) {
  writeFileSync(join(root, relativePath), generated);
}
